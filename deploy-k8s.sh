#!/bin/bash
# Script to deploy the Counter App to Kubernetes
# Run this script on a machine with kubectl access to your cluster

set -e

# Registry configuration
MASTER_IP=192.168.0.146
REGISTRY_PORT=5000
REGISTRY_URL="$MASTER_IP:$REGISTRY_PORT"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

echo -e "${GREEN}‚ò∏Ô∏è Deploying Counter App to Kubernetes...${NC}"

# Get script directory
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$SCRIPT_DIR"

echo -e "${BLUE}üìÅ Current directory: $(pwd)${NC}"

# Function to apply Kubernetes manifests
apply_k8s_manifests() {
    local path=$1
    local description=$2
    
    echo -e "${YELLOW}üöÄ Deploying $description...${NC}"
    
    if [ -d "$path" ]; then
        for file in "$path"/*.yaml; do
            if [ -f "$file" ]; then
                echo -e "   Applying $(basename "$file")..."
                kubectl apply -f "$file"
                if [ $? -ne 0 ]; then
                    echo -e "${RED}‚ùå Failed to apply $(basename "$file")${NC}"
                    return 1
                fi
            fi
        done
        echo -e "${GREEN}‚úÖ $description deployed successfully!${NC}"
        return 0
    else
        echo -e "${RED}‚ùå Path $path not found!${NC}"
        return 1
    fi
}

# Check if kubectl is available
if ! command -v kubectl &> /dev/null; then
    echo -e "${RED}‚ùå kubectl is not available or not configured!${NC}"
    echo -e "${YELLOW}   Please ensure kubectl is installed and configured to access your cluster.${NC}"
    exit 1
fi

# Check cluster connectivity
echo -e "${BLUE}üîç Checking cluster connectivity...${NC}"
if ! kubectl cluster-info &> /dev/null; then
    echo -e "${RED}‚ùå Cannot connect to Kubernetes cluster!${NC}"
    exit 1
fi

# Deploy in order: PostgreSQL -> Backend -> Frontend
echo -e "${BLUE}üìä Deployment order: PostgreSQL -> Backend -> Frontend${NC}"

# Deploy PostgreSQL
if ! apply_k8s_manifests "./k8s/postgres" "PostgreSQL"; then
    exit 1
fi

# Wait for PostgreSQL to be ready
echo -e "${YELLOW}‚è≥ Waiting for PostgreSQL to be ready...${NC}"
kubectl wait --for=condition=ready pod -l app=postgres --timeout=300s
if [ $? -ne 0 ]; then
    echo -e "${RED}‚ùå PostgreSQL deployment timed out!${NC}"
    exit 1
fi

# Deploy Backend
if ! apply_k8s_manifests "./k8s/backend" "Backend API"; then
    exit 1
fi

# Wait for Backend to be ready
echo -e "${YELLOW}‚è≥ Waiting for Backend to be ready...${NC}"
kubectl wait --for=condition=ready pod -l app=backend --timeout=300s
if [ $? -ne 0 ]; then
    echo -e "${RED}‚ùå Backend deployment timed out!${NC}"
    exit 1
fi

# Deploy Frontend
if ! apply_k8s_manifests "./k8s/frontend" "Frontend"; then
    exit 1
fi

# Wait for Frontend to be ready
echo -e "${YELLOW}‚è≥ Waiting for Frontend to be ready...${NC}"
kubectl wait --for=condition=ready pod -l app=frontend --timeout=300s
if [ $? -ne 0 ]; then
    echo -e "${RED}‚ùå Frontend deployment timed out!${NC}"
    exit 1
fi

# Show deployment status
echo -e "${BLUE}üìã Deployment Status:${NC}"
kubectl get pods -o wide
echo ""
kubectl get services
echo ""

# Show access information
echo -e "${GREEN}üéâ Deployment completed successfully!${NC}"
echo -e "${BLUE}üåê Access Information:${NC}"
echo -e "   Frontend URL: http://192.168.0.233:30080"
echo -e "   Backend API: http://192.168.0.233:30001 (internal only)"
echo ""
echo -e "${YELLOW}üîí Security Notes:${NC}"
echo -e "   ‚úÖ PostgreSQL: Only accessible within cluster"
echo -e "   ‚úÖ Backend: Accessible only from worker1 node"
echo -e "   ‚úÖ Frontend: Publicly accessible via LoadBalancer"
echo ""
echo -e "${BLUE}üìù Troubleshooting:${NC}"
echo -e "   kubectl logs -l app=postgres"
echo -e "   kubectl logs -l app=backend"
echo -e "   kubectl logs -l app=frontend"
echo ""
echo -e "${YELLOW}üìã Registry Information:${NC}"
echo -e "   Registry URL: $REGISTRY_URL"
echo -e "   Frontend Image: $REGISTRY_URL/counter-frontend:latest"
echo -e "   Backend Image: $REGISTRY_URL/counter-backend:latest"
